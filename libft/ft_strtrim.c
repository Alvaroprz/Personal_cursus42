#include "libft.h"

char	*ft_strtrim(char const *s1, char const *set)
{
	size_t	start;
	size_t	end;
	char	*aux;

	if (!s1 || !set)
		return (NULL);
	start = 0;
	while (s1[start] && ft_strchr(set, s1[start]))
		start++;
	end = ft_strlen(s1);
	while (end > start && ft_strchr(set, s1[end - 1]))
		end--;
	aux = (char *)malloc(sizeof(char) * (end - start + 1));
	if (!aux)
		return (NULL);
	ft_strlcpy(aux, s1 + start, end - start + 1);
	return (aux);
}
